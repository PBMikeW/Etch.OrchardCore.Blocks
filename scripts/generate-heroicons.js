const fs = require('fs');
const path = require('path');

const iconsDir = path.resolve(__dirname, '../node_modules/heroicons/24/outline');
const outputPath = path.resolve(
  __dirname,
  '../Assets/Editor.js/js/plugins/kbButton/heroicons-data.js'
);

if (!fs.existsSync(iconsDir)) {
  console.error('heroicons not found — run npm install first');
  process.exit(1);
}

const files = fs
  .readdirSync(iconsDir)
  .filter(f => f.endsWith('.svg'))
  .sort();

const icons = files.map(file => {
  const name = file.replace('.svg', '');
  let svg = fs
    .readFileSync(path.join(iconsDir, file), 'utf8')
    .replace(/\n/g, '')
    .trim();
  // Inject explicit width/height so SVG is always constrained even without CSS
  svg = svg.replace('<svg ', '<svg width="20" height="20" ');
  return { name, svg };
});

const output = `// Auto-generated by scripts/generate-heroicons.js — do not edit
const HEROICONS = ${JSON.stringify(icons, null, 2)};
export default HEROICONS;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output, 'utf8');
console.log(`Generated ${icons.length} heroicons → ${outputPath}`);
